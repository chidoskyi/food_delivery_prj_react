export const mealData = []; // Holds fetched meal data.

        class Meal {
          constructor(mealDetails) {
            this.id = mealDetails.id;
            this.image = mealDetails.image;
            this.title = mealDetails.title;
            this.category = mealDetails.category;
            this.image_url = mealDetails.image_url;
          }
        }
        
        export async function getMeals() {
          try {
            const response = await fetch(`${import.meta.env.VITE_API_URL}/meals/`);
            const data = await response.json();
        
            // Clear existing data and populate with new instances of Meal.
            mealData.length = 0; // Clear the array.
            mealData.push(...data.map((mealDetails) => new Meal(mealDetails)));
        
            return mealData; // Return the updated data.
          } catch (error) {
            console.error("Error fetching meals:", error);
            throw error;
          }
        }
        